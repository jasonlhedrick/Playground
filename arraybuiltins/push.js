/*
Array.push(val) adds the value ${val} onto the end of the array.
Then returns the updated length of the array.
*/

const arr = [3, 2, 4, 10];

console.log(`arr before applying .push(4) on it ${arr}`);
console.log(`The return value of .push(4) : ${arr.push(4)}`);
console.log(`arr after applying .push(4) on it ${arr}`);